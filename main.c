uint32_t image[256][256] = {0};

static uint32_t make_rgba(uint8_t r, uint8_t g, uint8_t b, uint8_t a) {
    return ((uint32_t)r << 24) | ((uint32_t)g << 16) | ((uint32_t)b << 8) | (uint32_t)a;
}

static void fill_image() {
    for (int y = 0; y < 256; y++) {
        for (int x = 0; x < 256; x++) {
            uint8_t r = x;
            uint8_t g = y;
            uint8_t b = (x + y) / 2;
            uint8_t a = 255;

            image[x][y] = make_rgba(r, g, b, a);
        }
    }
}

